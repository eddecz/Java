public class Condição {
  public static void main(String[] args) {
    int x = 1;
      
    while (x < 3) {
      System.out.print("Pa");
      x = x + 1;
    }
    if (x == 3) {
      System.out.print("razzi");
    }
  }
}

/*Utilizando a condição que enquanto (while) x for menor
  que 3 (x < 3) vai ser retornado um valor ("pa") até
  essa condição ser quebrada*/

/*Se (if) a condição for quebrada (x == 3) outro valor
  será retornado ("razzi")*/
